/**
 * 生成 1000 字內的深度繁體中文解析
 */
export function generateInterpretation(result: NumerologyResult): string {
  const {
    lifeNumber,
    talentNumbers,
    zodiacSign,
    digitCounts,
    missingDigits,
    activeLines,
  } = result;

  const parts: string[] = [];

  // --- 1. 生命核心 (生命數) ---
  const lifePersonality: Record<number, string> = {
    1: "你天生具備獨立開創的能量，內心有股驅動自己往前的力量，傾向用自己的方式解決問題。你不需要追隨別人的腳步，因為你本身就是引路人。",
    2: "你天生對人際關係極其敏銳，能細膩感受他人的情緒波動。你擅長在衝突中扮演協調者，溫潤但內心有其堅持，是團隊中不可或缺的潤滑劑。",
    3: "你對世界充滿好奇，表達力與創意是你最大的武器。你能用言語或創意賦予枯燥事務新生命，但要小心別讓過多的點子分散了你的專注力。",
    4: "你重視秩序與穩定，是所有人眼中最可靠的後盾。你做事踏實且極具耐心，能將混亂整理為規律，但有時會因為過度追求安全感而顯得保守。",
    5: "你對自由的渴望深植於基因，變化與探索是你生命活力的來源。你擁有極強的環境適應力，能快速切換視角，但也常面臨定性與深度積累的挑戰。",
    6: "你對家庭、夥伴與社會責任有深刻認同。你具備審美天賦，天生有照顧他人的職志，但要提醒自己，在療癒他人之前，必須先圓滿自己。",
    7: "你擁有深邃的內心世界，喜歡深入思考而不流於表面。你善於分析、觀察入微，外表雖然冷靜，內在卻對真理有著近乎固執的追求。",
    8: "你對目標與成就感有著強烈執行力，具備天然的商業直覺與資源整合能力。你注定要在物質世界中創造結果，但也要學會在情感上練習柔軟。",
    9: "你有著超越常人的博愛精神與宏觀視野。你的理想主義能激勵他人，但也容易因為看見太多大局而忽略了當下的現實細節。",
  };

  parts.push(
    `【生命核心】\n你的生命數為 ${lifeNumber}。這代表${lifePersonality[lifeNumber] ?? "你有獨特的生命能量等待探索。"}`
  );

  // --- 2. 天賦與星座色彩 ---
  const talentText = talentNumbers.length === 2
    ? `天賦數 ${talentNumbers[0]} 與 ${talentNumbers[1]} 的組合，賦予了你兼具兩者的行動特質，讓你在處理事情時能有更彈性的切換空間。`
    : `單一天賦數 ${talentNumbers[0]} 則高度強化了你核心性格中的精純能量。`;

  const zodiacInfluence: Record<string, string> = {
    牡羊座: "配合牡羊座的果決，你做事不拖泥帶水，具備戰士般的衝勁。",
    金牛座: "金牛座穩定頻率讓你更懂得在物質世界中穩紮穩打，建立堅實基礎。",
    雙子座: "雙子座的靈動為你帶來極佳的訊息處理能力，思維跳躍且反應快速。",
    巨蟹座: "巨蟹座深化的保護本能，讓你在人際交往中具備更強的同理包容力。",
    獅子座: "獅子座為你帶來更強的自我表現力與領導氣場，讓你習慣站上舞台。",
    處女座: "處女座的細膩則讓你更注重細節與邏輯，追求極致的準確與品質。",
    天秤座: "天秤座的和諧頻率讓你更重視合作的平衡，優雅地處理人際衝突。",
    天蠍座: "天蠍座的洞察力讓你擁有極強的抗壓力，擅長在危機中翻轉局勢。",
    射手座: "射手座的樂觀擴張了你的視野，讓你總能看見事情正向的可能性。",
    摩羯座: "摩羯座的務實給予你極強的耐力，願意為了長期目標忍受孤獨。",
    水瓶座: "水瓶座的獨立思考讓你具備前瞻性，能看見當前時代還未成形的標竿。",
    雙魚座: "雙魚座的浪漫想像力讓你的心靈世界極其豐富，擁有強大的創造能量。",
  };

  parts.push(
    `【個人特質】\n${talentText}\n配合${zodiacSign}的特點，${zodiacInfluence[zodiacSign] ?? ""}`
  );

  // --- 3. 九宮格能量分布 (重點強化) ---
  parts.push(`【九宮格能量深度分析】`);

  // 強勢能量
  const dominantDigits = Object.entries(digitCounts)
    .filter(([, c]) => c >= 3)
    .map(([d]) => Number(d));

  if (dominantDigits.length > 0) {
    const dominantDesc = dominantDigits.map(d => {
      const t = DIGIT_TALENTS[d];
      return `● 「${t.title}」（數字 ${d}）：這項特質在你的生命中極為強勢。${t.details} 這是你最強大的工具，但也可能是你性格中容易讓他人感到壓力的部分，建議有意識地調節輸出的力度。`;
    }).join("\n");
    parts.push(`目前你的能量過於集中於：\n${dominantDesc}`);
  }

  // 缺失能量 (需求點)
  if (missingDigits.length > 0) {
    const missingDesc = missingDigits.map(d => {
      const t = DIGIT_TALENTS[d];
      return `● 需要強化的「${t.title}」：在目前藍圖中，對應${t.desc}的能量相對較弱。代表你在處理相關事務時，可能較容易感到不自在或缺乏直覺。例如：${t.details.split("；")[0]}，這是你未來可以透過刻意練習來補漏的方向。`;
    }).slice(0, 3).join("\n"); // 最多列 3 個免得太長
    
    parts.push(`針對你較不熟悉的領域（缺失數），建議關注以下方向的平衡：\n${missingDesc}`);
    if (missingDigits.length > 3) {
      parts.push(`此外，包含 ${missingDigits.slice(3).join("、")} 代表的能量也尚待開發，你可以在生命的不同階段逐步整合這些特質。`);
    }
  }

  // --- 4. 主要連線 ---
  if (activeLines.length > 0) {
    const lineNames: Record<string, string> = {
      "1-2-3": "「思維軸」", "4-5-6": "「意志軸」", "7-8-9": "「行動軸」",
      "1-4-7": "「計畫軸」", "2-5-8": "「感性軸」", "3-6-9": "「創造軸」",
      "1-5-9": "「決心軸」", "3-5-7": "「靈感軸」",
    };
    const lineFullDescs: Record<string, string> = {
      "1-2-3": "顯示你具備連貫的思考邏輯，能在表達中展現清晰的分析力。",
      "4-5-6": "意味著你有強大的意志貫徹力，面對目標時具備不屈不撓的精神。",
      "7-8-9": "代表你具備極強的落地執行力，不只是空談，更能將成果實例化。",
      "1-4-7": "你在長遠規劃與穩健推進上具備天然優勢，適合管理大型專案。",
      "2-5-8": "你的情感感知與直覺連動性極佳，能敏銳捕捉周遭的人力與資源變化。",
      "3-6-9": "代表你的創意思維與博愛精神相輔相成，能產出具有溫度的作品或方案。",
      "1-5-9": "這是強大的創業家連線，目標明確、意志堅定，遇到困難會正面突破。",
      "3-5-7": "顯示你具備過人的洞察力與預見性，常能看見他人看不見的機會點。",
    };

    const lineDescs = activeLines
      .map((k) => `${lineNames[k] ?? k}${lineFullDescs[k] ?? ""}`)
      .join("\n");
    parts.push(`【主要優勢通道】\n目前你已形成以下連線：\n${lineDescs}`);
  } else {
    parts.push(
      "【潛力整合期】\n目前你的九宮格尚未形成完整連線。這並非劣勢，而是代表你的能量正處於高度分散與靈活的狀態，你可以根據當下的環境需求，自由地調用各個數字的特質。這段時期是很好的資源儲備期，隨著生命經驗的累積，這些點會逐步連線成面。"
    );
  }

  // --- 5. 成長建議 (補充長度) ---
  const growthSuggestions: Record<number, string> = {
    1: "你不需要時刻當一個孤狼。學會接受他人的貢獻，你會發現眾人的智慧能讓你的開創性想法走得更遠、更穩固。",
    2: "在關懷他人之前，請先學會肯定自己的價值。你的敏感是天賦，但不要讓它成為讓你陷入情緒黑洞的負擔。",
    3: "創意的真諦在於最終的完成。試著在迸發靈感的同時，找一位可靠的夥伴協助你執行，這能讓你的天份真正兌現成價值。",
    4: "打破安全感的舒適圈有時是必要的。當你覺得一切都在掌控中時，嘗試引入一點點不確定性，這會是生意的轉機或人格的突破點。",
    5: "在追求廣度的探索之餘，選一件最讓你有熱情的事進行深度鑽研。深耕一英里的成果，往往比走一英里遠的見聞更能賦予你內在的力量。",
    6: "你的愛與責任感非常有價值，但請記得適時卸下重擔。你不需要拯救每一個人，允許世界偶爾有些混亂，換取你靈魂的休息空間。",
    7: "思考是你的力量，但過度思辨可能導致行動癱瘓。在分析清楚 80% 的真相後就大膽邁步吧，剩下的答案會在行動的過程中浮現。",
    8: "你對資源的掌控力很強，但真正的連結來自脆弱。在展現權力的同時，若能偶爾露出你柔軟的一面，你會獲得更真實的跟隨者。",
    9: "理想與夢想需要細碎的塵埃來支撐。不要輕視日常琐事，當你願意在小事上付出耐心，你的宏觀藍圖才會真正從空想變成現實。",
  };

  parts.push(
    `【給你的成長指南】\n${growthSuggestions[lifeNumber] ?? "持續聆聽內心的聲音，它是你穿越數字迷霧最可靠的指南針。"}\n\n生命的意義在於覺察。透過理解這些數字的頻率，我們不是要被其限制，而是要學會駕馭。願你在數字的節奏中，找到最平衡的自我。`
  );

  return parts.join("\n\n");
}
